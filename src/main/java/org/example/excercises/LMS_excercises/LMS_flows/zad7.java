package org.example.excercises.LMS_excercises.LMS_flows;
/*
Zadanie 7

    W pliku Main07.java stwórz dwie pętle i wypisz wartości ich liczników
    w każdej iteracji dla i < 3 oraz j < 3.
    Wykorzystaj: System.out.println("i = " + i + " j = " + j);
    Zadanie rozwiąż używając dwóch pętli for.
 */

public class zad7 {
    public static void main(String[] args) {

        for (int i = 0; i < 3; i++) {

            for (int j = 0; j < 3; j++) {
                System.out.println("i = " + i + " j = " + j);
            }
        }
    }
}
